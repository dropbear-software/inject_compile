import 'package:code_builder/code_builder.dart';
import '../models/injected_type.dart';
import '../models/injector_graph.dart';
import '../models/lookup_key.dart';
import '../models/symbol_path.dart';

/// A generator that creates concrete implementations of [Injector]-annotated classes.
class InjectorGenerator {
  final InjectorGraph graph;
  final SymbolPath injectorPath;

  InjectorGenerator(this.graph, this.injectorPath);

  /// The generated library containing the injector implementation.
  Library generate() {
    return Library(
      (b) => b
        ..body.add(_generateInjectorClass())
        ..generatedByComment =
            'Generated by inject_compile. Do not edit by hand.',
    );
  }

  Class _generateInjectorClass() {
    final className = '${injectorPath.symbol}\$Injector';

    return Class(
      (b) => b
        ..name = className
        ..extend = refer(
          injectorPath.symbol,
          injectorPath.toDartUri().toString(),
        )
        ..fields.addAll(_generateModuleFields())
        ..fields.addAll(_generateSingletonFields())
        ..fields.addAll(_generateAsyncFields())
        ..constructors.add(_generateConstructor())
        ..methods.add(_generateStaticCreate())
        ..methods.addAll(_generateInjectorProviders())
        ..methods.addAll(_generateCreateMethods()),
    );
  }

  List<Field> _generateAsyncFields() {
    return _getAsyncDependenciesInOrder().map((key) {
      final type = _referenceForKey(key);
      return Field(
        (f) => f
          ..name = '_async${_lookupKeyName(key)}'
          ..type = TypeReference(
            (t) => t
              ..symbol = type.symbol
              ..url = type.url
              ..isNullable = true,
          )
          ..modifier = FieldModifier.var$,
      );
    }).toList();
  }

  List<Field> _generateModuleFields() {
    return graph.modules.map((m) {
      return Field(
        (f) => f
          ..name = '_${_decapitalize(m.symbol)}'
          ..type = refer(m.symbol, m.toDartUri().toString())
          ..modifier = FieldModifier.final$,
      );
    }).toList();
  }

  List<Field> _generateSingletonFields() {
    return graph.mergedDependencies.entries
        .where((e) => e.value.isSingleton && !e.value.isAsynchronous)
        .map((e) {
          final key = e.key;
          final type = _referenceForKey(key);
          return Field(
            (f) => f
              ..name = '_singleton${_lookupKeyName(key)}'
              ..type = TypeReference(
                (t) => t
                  ..symbol = type.symbol
                  ..url = type.url
                  ..isNullable = true,
              )
              ..modifier = FieldModifier.var$,
          );
        })
        .toList();
  }

  Constructor _generateConstructor() {
    return Constructor(
      (b) => b
        ..initializers.addAll(
          graph.modules.map((m) {
            final fieldName = '_${_decapitalize(m.symbol)}';
            final paramName = _decapitalize(m.symbol);
            return refer(fieldName).assign(refer(paramName)).code;
          }),
        )
        ..requiredParameters.addAll(
          graph.modules.map((m) {
            final name = _decapitalize(m.symbol);
            return Parameter(
              (p) => p
                ..name = name
                ..type = refer(m.symbol, m.toDartUri().toString()),
            );
          }),
        ),
    );
  }

  Method _generateStaticCreate() {
    final className = '${injectorPath.symbol}\$Injector';

    return Method(
      (m) => m
        ..name = 'create'
        ..static = true
        ..returns = TypeReference(
          (b) => b
            ..symbol = 'Future'
            ..types.add(
              refer(injectorPath.symbol, injectorPath.toDartUri().toString()),
            ),
        )
        ..modifier = MethodModifier.async
        ..requiredParameters.addAll(
          graph.modules.map((m) {
            return Parameter(
              (p) => p
                ..name = _decapitalize(m.symbol)
                ..type = refer(m.symbol, m.toDartUri().toString()),
            );
          }),
        )
        ..body = Block((b) {
          final injectorVar = refer('injector');
          b.statements.add(
            declareFinal('injector')
                .assign(
                  refer(className).newInstance(
                    graph.modules.map((m) => refer(_decapitalize(m.symbol))),
                  ),
                )
                .statement,
          );

          // Handle async pre-instantiation.
          final asyncDeps = _getAsyncDependenciesInOrder();
          for (final key in asyncDeps) {
            final dep = graph.mergedDependencies[key]!;
            b.statements.add(
              injectorVar
                  .property('_async${_lookupKeyName(key)}')
                  .assign(
                    _generateInstantiatingExpression(
                      dep,
                      scope: 'injector',
                    ).awaited,
                  )
                  .statement,
            );
          }

          b.statements.add(injectorVar.returned.statement);
        }),
    );
  }

  List<Method> _generateInjectorProviders() {
    return graph.injectorProviders.map((p) {
      return Method(
        (m) => m
          ..name = p.name
          ..returns = _referenceForType(p.resultType)
          ..type = p.isGetter ? MethodType.getter : null
          ..annotations.add(refer('override'))
          ..body = _generateProviderBody(p.resultType),
      );
    }).toList();
  }

  Code _generateProviderBody(InjectedType type) {
    final createCall = refer(
      '_create${_lookupKeyName(type.lookupKey)}',
    ).call([]);
    return type.isProvider
        ? refer('_create${_lookupKeyName(type.lookupKey)}').code
        : createCall.code;
  }

  List<Method> _generateCreateMethods() {
    return graph.mergedDependencies.entries.map((e) {
      final key = e.key;
      final dep = e.value;

      return Method(
        (m) => m
          ..name = '_create${_lookupKeyName(key)}'
          ..returns = _referenceForKey(key)
          ..body = _generateDependencyBody(key, dep),
      );
    }).toList();
  }

  Code _generateDependencyBody(LookupKey key, ResolvedDependency dep) {
    if (dep.isAsynchronous) {
      return refer(
        '_async${_lookupKeyName(key)}',
      ).nullChecked.returned.statement;
    }
    if (dep.isSingleton) {
      final fieldName = '_singleton${_lookupKeyName(key)}';
      return refer(
        fieldName,
      ).assignNullAware(_generateInstantiatingExpression(dep)).code;
    }
    return _generateInstantiatingExpression(dep).code;
  }

  Expression _generateInstantiatingExpression(
    ResolvedDependency dep, {
    String scope = 'this',
  }) => switch (dep) {
    DependencyProvidedByModule() => _instantiateFromModule(dep, scope),
    DependencyProvidedByInjectable() => _instantiateFromInjectable(dep, scope),
  };

  Expression _instantiateFromModule(
    DependencyProvidedByModule dep,
    String scope,
  ) {
    final moduleField = refer('_${_decapitalize(dep.module.symbol)}');
    final target = scope == 'this'
        ? moduleField
        : refer(scope).property(moduleField.symbol!);
    return target
        .property(dep.provider.name)
        .call(
          dep.dependencies.map(
            (d) => _generateDependencyInvocation(d, scope: scope),
          ),
        );
  }

  Expression _instantiateFromInjectable(
    DependencyProvidedByInjectable dep,
    String scope,
  ) {
    final type = refer(
      dep.summary.clazz.symbol,
      dep.summary.clazz.toDartUri().toString(),
    );
    final constructorName = dep.summary.constructor.name;
    final constructor = (constructorName.isEmpty || constructorName == 'new')
        ? type
        : type.newInstanceNamed(constructorName, []);
    return constructor.call(
      dep.dependencies.map(
        (d) => _generateDependencyInvocation(d, scope: scope),
      ),
    );
  }

  Expression _generateDependencyInvocation(
    InjectedType dep, {
    String scope = 'this',
  }) {
    final methodName = '_create${_lookupKeyName(dep.lookupKey)}';
    if (scope == 'this') {
      final createMethod = refer(methodName);
      return dep.isProvider ? createMethod : createMethod.call([]);
    } else {
      final createMethod = refer(scope).property(methodName);
      return dep.isProvider ? createMethod : createMethod.call([]);
    }
  }

  Reference _referenceForKey(LookupKey key) {
    return _referenceForSymbolPath(key.root);
  }

  Reference _referenceForSymbolPath(SymbolPath path) {
    if (path.typeArguments.isEmpty) {
      return refer(
        path.symbol,
        path.isGlobal ? null : path.toDartUri().toString(),
      );
    }
    return TypeReference(
      (b) => b
        ..symbol = path.symbol
        ..url = path.isGlobal ? null : path.toDartUri().toString()
        ..types.addAll(path.typeArguments.map(_referenceForSymbolPath)),
    );
  }

  Reference _referenceForType(InjectedType type) {
    final keyRef = _referenceForKey(type.lookupKey);
    if (type.isProvider) {
      return FunctionType((b) => b..returnType = keyRef);
    }
    return keyRef;
  }

  String _lookupKeyName(LookupKey key) {
    final qualifier = key.qualifier != null
        ? _capitalize(key.qualifier!.symbol)
        : '';
    return '$qualifier${_capitalize(key.root.symbol)}';
  }

  String _capitalize(String s) => s[0].toUpperCase() + s.substring(1);
  String _decapitalize(String s) => s[0].toLowerCase() + s.substring(1);

  List<LookupKey> _getAsyncDependenciesInOrder() {
    final results = <LookupKey>[];
    final visited = <LookupKey>{};

    void visit(LookupKey key) {
      if (visited.contains(key)) return;
      visited.add(key);

      final dep = graph.mergedDependencies[key];
      if (dep == null) return;

      for (final child in dep.dependencies) {
        visit(child.lookupKey);
      }

      if (dep.isAsynchronous) {
        results.add(key);
      }
    }

    for (final provider in graph.injectorProviders) {
      visit(provider.resultType.lookupKey);
    }
    return results;
  }
}
